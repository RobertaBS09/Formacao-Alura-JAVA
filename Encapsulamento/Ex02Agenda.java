package Encapsulamento;

public class Ex02Agenda {
    String nome;
    String telefone;

    public Ex02Agenda(String nome, String telefone) {
        this.nome = nome;
        this.telefone = telefone;
    }

    public String getNome() {
        return nome;
    }

    public String getTelefone() {
        return telefone;
    }
}
